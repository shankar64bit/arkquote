import '../../../../domain/entities/equipment/acb/acb.dart';

class AcbModel extends Acb {
  const AcbModel({
    final String etype,
    final String designation,
    final String location,
    final String panel,
    final String serialNo,
    final String make,
    final String type,
    final int ratedSystemVoltage,
    final int ratedSystemCurrent,
    final int yom,
    final DateTime dateOfTesting,
    final DateTime dueDateOfTesting,
    final int noOfPoles,
    final int iecStandard,
    final int trNo,
    final int id,
    final int databaseID,
    final String TestedBy,
    final String VerifiedBy,
    final String WitnessedBy,
    final DateTime lastUpdated,
    final double motorVoltage,
    final double closeCoilTripVolt,
  }) : super(
          etype: etype,
          dateOfTesting: dateOfTesting,
          designation: designation,
          location: location,
          panel: panel,
          serialNo: serialNo,
          make: make,
          type: type,
          ratedSystemCurrent: ratedSystemCurrent,
          ratedSystemVoltage: ratedSystemVoltage,
          yom: yom,
          dueDateOfTesting: dueDateOfTesting,
          noOfPoles: noOfPoles,
          iecStandard: iecStandard,
          trNo: trNo,
          id: id,
          databaseID: databaseID,
          TestedBy: TestedBy,
          VerifiedBy: VerifiedBy,
          WitnessedBy: WitnessedBy,
          lastUpdated: lastUpdated,
          motorVoltage: motorVoltage,
          closeCoilTripVolt: closeCoilTripVolt,
        );

  @override
  List<Object> get props => [
        etype,
        designation,
        location,
        panel,
        serialNo,
        make,
        type,
        ratedSystemCurrent,
        ratedSystemVoltage,
        yom,
        dateOfTesting,
        dueDateOfTesting,
        noOfPoles,
        iecStandard,
        trNo,
        id,
        databaseID,
        TestedBy,
        VerifiedBy,
        WitnessedBy,
        lastUpdated,
        motorVoltage,
        closeCoilTripVolt,
      ];

  @override
  bool get stringify => true;

  Map<String, dynamic> toJson() {
    return {
      'etype': etype,
      'designation': designation,
      'location': location,
      'panel': panel,
      'serialNo': serialNo,
      'make': make,
      'type': type,
      'ratedSysCur': ratedSystemCurrent,
      'ratedSysVol': ratedSystemVoltage,
      'yom': yom,
      'dateOfTesting': dateOfTesting,
      'DueDateOfTesting': dueDateOfTesting,
      'noOfPoles': noOfPoles,
      'iecStandard': iecStandard,
      'trNo': trNo,
      'id': id,
      'databaseID': databaseID,
      'testedBy': TestedBy,
      'verifiedBy': VerifiedBy,
      'WitnessedBy': WitnessedBy,
      'updateDate': lastUpdated,
      'motarVoltage': motorVoltage,
      'closeCoilTripVolt': closeCoilTripVolt,
    };
  }

  factory AcbModel.fromJson(Map<String, dynamic> json) {
    return AcbModel(
      etype: json['etype'],
      designation: json['designation'],
      location: json['location'],
      panel: json['panel'],
      serialNo: json['serialNo'],
      make: json['make'],
      type: json['type'],
      ratedSystemCurrent: json['ratedSysCur'],
      ratedSystemVoltage: json['ratedSysVol'],
      yom: json['yom'],
      dateOfTesting: json['dateOfTesting'],
      dueDateOfTesting: json['DueDateOfTesting'],
      noOfPoles: json['noOfPoles'],
      iecStandard: json['iecStandard'],
      trNo: json['trNo'],
      id: json['id'],
      databaseID: json['databaseID'],
      TestedBy: json['testedBy'],
      VerifiedBy: json['verifiedBy'],
      WitnessedBy: json['WitnessedBy'],
      lastUpdated: json['updateDate'],
      motorVoltage: json['motarVoltage'],
      closeCoilTripVolt: json['closeCoilTripVolt'],
    );
  }
}
